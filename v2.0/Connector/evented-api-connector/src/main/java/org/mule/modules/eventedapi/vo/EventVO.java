package org.mule.modules.eventedapi.vo;

public class EventVO {
	
	private String eventId;
	private String eventType;
	private String eventDomain;
	private String eventCategory;
	private String eventPayloadType;
	private String eventName;
	private String eventNS;
	public String getEventId() {
		return eventId;
	}
	public void setEventId(String eventId) {
		this.eventId = eventId;
	}
	public String getEventType() {
		return eventType;
	}
	public void setEventType(String eventType) {
		this.eventType = eventType;
	}
	public String getEventDomain() {
		return eventDomain;
	}
	public void setEventDomain(String eventDomain) {
		this.eventDomain = eventDomain;
	}
	public String getEventCategory() {
		return eventCategory;
	}
	public void setEventCategory(String eventCategory) {
		this.eventCategory = eventCategory;
	}
	public String getEventPayloadType() {
		return eventPayloadType;
	}
	public void setEventPayloadType(String eventPayloadType) {
		this.eventPayloadType = eventPayloadType;
	}
	public String getEventName() {
		return eventName;
	}
	public void setEventName(String eventName) {
		this.eventName = eventName;
	}
	public String getEventNS() {
		return eventNS;
	}
	public void setEventNS(String eventNS) {
		this.eventNS = eventNS;
	}
	
	
	
	

}
